                <h2 class="callout_headline">What Is Failed Back Surgery Syndrome (FBSS)?</h2>
                <p class="callout_text">Failed Back Surgery Syndrome is not a syndrome at all. Rather, it is a blanket
                    term for patients that have undergone back or neck surgery and still find that they have pain. FBSS
                    may also not necessarily mean your surgery truly “failed” in a medical sense. In some cases, your
                    surgery may have been successful, but introduced or uncovered other issues that resulted in
                    additional pain.</p><br>
